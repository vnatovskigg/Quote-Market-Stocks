const insert = async (article, segment) => {
  let { title, 
    autoGeneratedSummary, 
    byline, 
    internalID, 
    published, 
    thumbnailImage, 
    shortURL 
  } = article;

  let res = await fetch(`http://localhost:8888/api/articles/`, {
    method: "POST",
    body: JSON.stringify({
      title,
      summary: autoGeneratedSummary,
      published: published,
      author: byline || "Unknown",
      url: shortURL,
      thumbnail: thumbnailImage,
      uid: internalID,
      segment
    }),
    headers: {
      "Content-Type": "application/json",
    },
  })
  console.log("RES ", res);
}

